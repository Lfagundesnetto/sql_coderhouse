data_pedido DATE, 
data_entrega DATE, 
preco DECIMAL, 
frete DECIMAL, 
preco_total DECIMAL, 
forma_de_pagamento TEXT, 
endereco_entrega VARCHAR(50),
estado_entrega TEXT
