CREATE TABLE usuario_tabela (
    usuario_id INT PRIMARY KEY AUTO_INCREMENT,
    nome TEXT,
    sobrenome TEXT,
    data_nascimento DATE,
    endereco VARCHAR(50),
    num_cep VARCHAR(10),
    nom_uf TEXT,
    email VARCHAR(50),
    num_sapato INT,
    genero VARCHAR(10),
